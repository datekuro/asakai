class User < ApplicationRecord
  # Include default devise modules. Others available are:
  # :confirmable, :lockable, :timeoutable, :trackable and :omniauthable
  devise :database_authenticatable, :registerable,
         :recoverable, :rememberable, :validatable,
         :confirmable, :timeoutable, :trackable, :invitable

  has_many :articles
  has_many :comments
  has_many :post_comment_articles, through: :comments, source: :articles
  has_many :received_comments, through: :articles, source: :comments

  validates :nickname, presence: true, length: { maximum: 50 }
  validates :email,
            presence: true,
            length: { maximum: 100 },
            format: { with: Settings.application.email.regexp }

  def display_name
    nickname || email
  end
end
